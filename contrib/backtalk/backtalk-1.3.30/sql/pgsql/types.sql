# SQL Command Definitions:  PostgreSQL / type definitions

# login id
&type_login = varchar(32)

# user id number
&type_uid = int4

# group id number
&type_gid = int4

# encrypted password
&type_pass = varchar(16)

# user's full name
&type_fname = varchar(80)

# group name
&type_gname = varchar(32)

# directory path
&type_dir = varchar(80)

# boolean
&type_flag = int2

# Unix time (seconds since 1970)
&type_time = int4

# ip address of a host
&type_ip = inet

# mime type
&type_mimetype = varchar(35)

# conference name
&type_confname = varchar(50)

# attachment handle
&type_athand = varchar(50)
